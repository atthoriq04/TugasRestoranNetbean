            String Masuk = "UPDATE Menu SET kategori_menu = ?, nama_menu = ?, harga_menu = ? WHERE id_menu = ?";
            pst = conn.prepareStatement(Masuk);
            pst.setString(1,kat1.getText());
            pst.setString(2,nam.getText());
